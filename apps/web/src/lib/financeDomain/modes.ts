/**
 * Режимы оплаты смены.
 *
 * Сейчас в системе фактически используется схема «процент + гарантия».
 * Остальные режимы зарезервированы под будущие сценарии и помогают
 * формализовать расширение формул в financeDomain.
 */
export type PaymentMode =
    | 'percent_only'           // только процент от выручки, без учета гарантии
    | 'percent_with_guarantee' // процент + гарантия (текущий режим по умолчанию)
    | 'fixed_per_shift'        // фиксированная сумма за смену (потенциально)
    | 'custom';                // кастомные/смешанные схемы

